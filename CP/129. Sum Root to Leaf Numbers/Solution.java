class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}

class Solution {
    int sum = 0;

    public int sumNumbers(TreeNode root) {
        dfs(root, 0);
        return sum;
    }

    void dfs(TreeNode node, int path) {
        if (node == null)
            return;
        path = path * 10 + node.val;
        if (node.left == null && node.right == null) {
            sum += path;
        }
        dfs(node.left, path);
        dfs(node.right, path);
    }
}